#include<iostream>
using namespace std;
int main()
{
	cout<<"size of integer "<<sizeof(int)<<endl;
	cout<<"size of float "<<sizeof(float)<<endl;
	cout<<"size of string "<<sizeof(string)<<endl;
	cout<<"size of character "<<sizeof(char)<<endl;
	cout<<"size of double "<<sizeof(double)<<endl;
	return 0;
	
	
	
}
